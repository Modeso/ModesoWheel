import UIKit

extension UIColor {
	@nonobjc class var steel: UIColor {
		return UIColor(red: 129.0 / 255.0, green: 129.0 / 255.0, blue: 134.0 / 255.0, alpha: 1.0)
	}

	@nonobjc class var coolBlue: UIColor {
		return UIColor(red: 77.0 / 255.0, green: 146.0 / 255.0, blue: 200.0 / 255.0, alpha: 1.0)
	}
	@nonobjc class var whiteTwo: UIColor {
		return UIColor(white: 251.0 / 255.0, alpha: 1.0)
	}
}
