import UIKit

extension UIColor {
	@nonobjc class var mbeSteel: UIColor {
		return UIColor(red: 129.0 / 255.0, green: 129.0 / 255.0, blue: 134.0 / 255.0, alpha: 1.0)
	}

	@nonobjc class var mbeBlack: UIColor {
		return UIColor(white: 51.0 / 255.0, alpha: 1.0)
	}

	@nonobjc class var mbeLightBlueGrey: UIColor {
		return UIColor(red: 166.0 / 255.0, green: 200.0 / 255.0, blue: 227.0 / 255.0, alpha: 1.0)
	}

	@nonobjc class var mbeCoolBlue: UIColor {
		return UIColor(red: 77.0 / 255.0, green: 146.0 / 255.0, blue: 200.0 / 255.0, alpha: 1.0)
	}

	@nonobjc class var mbeDuskBlue: UIColor {
		return UIColor(red: 34.0 / 255.0, green: 87.0 / 255.0, blue: 125.0 / 255.0, alpha: 1.0)
	}

	@nonobjc class var mbeWhite: UIColor {
		return UIColor(white: 1.0, alpha: 1.0)
	}

	@nonobjc class var mbeWhiteTwo: UIColor {
		return UIColor(white: 251.0 / 255.0, alpha: 1.0)
	}

	@nonobjc class var mbeFadedBlue: UIColor {
		return UIColor(red: 113.0 / 255.0, green: 168.0 / 255.0, blue: 211.0 / 255.0, alpha: 1.0)
	}

	@nonobjc class var mbePaleGrey: UIColor {
		return UIColor(red: 229.0 / 255.0, green: 239.0 / 255.0, blue: 246.0 / 255.0, alpha: 1.0)
	}

	@nonobjc class var mbeLightBlueGrey20: UIColor {
		return UIColor(red: 166.0 / 255.0, green: 200.0 / 255.0, blue: 227.0 / 255.0, alpha: 0.2)
	}

	@nonobjc class var mbeWarning: UIColor {
		return UIColor(red: 162.0 / 255.0, green: 0.0 / 255.0, blue: 20.0 / 255.0, alpha: 1.0)
	}
}
